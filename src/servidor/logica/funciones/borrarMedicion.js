module.exports = async function borrarMedicion(datos) {
    const textoSQL = "DELETE FROM mediciones WHERE id_medicion = $id_medicion";
    const valoresParaSQL = { $id_medicion: datos.id_medicion };

    return new Promise((resolve, reject) => {
        borrarMedicion.conexion.run(textoSQL, valoresParaSQL, function(err) {
            err ? reject(err) : resolve();
        });
    });
} // ()


