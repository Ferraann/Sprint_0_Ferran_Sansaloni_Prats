// ----------------------------------------------------------
//    long: tiempo --> esperar()
//                        <--
// ----------------------------------------------------------
void esperar(long tiempo) {
  delay(tiempo);
} // ()

// ----------------------------------------------------------
// Clase LED
// ----------------------------------------------------------
class LED {
private:
  int numeroLED;
  bool encendido;
public:

  // .........................................................
  // Constructor
  // .........................................................
  LED(int numero)
    : numeroLED(numero), encendido(false) {
    pinMode(numeroLED, OUTPUT);
    apagar();
  }

  // .........................................................
  //    encender() 
  //      <--
  // .........................................................
  void encender() {
    digitalWrite(numeroLED, HIGH);
    encendido = true;
  }

  // .........................................................
  //    apagar() 
  //      <--
  // .........................................................
  void apagar() {
    digitalWrite(numeroLED, LOW);
    encendido = false;
  }

  // .........................................................
  //    alterar() 
  //      <--
  // .........................................................
  void alternar() {
    if (encendido) {
      apagar();
    } else {
      encender();
    }
  }  // ()

  // .........................................................
  //    long: tiempo --> brillar()
  //                        <--
  // .........................................................
  void brillar(long tiempo) {
    encender();
    esperar(tiempo);
    apagar();
  }
};  // class

